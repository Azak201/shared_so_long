#include "libft.h"

int	ft_putstr(char *s)
{
	size_t		x;

	x = 0;
	if (!s)
	{
		x = ft_putstr("(null)");
		return (x);
	}
	while (s[x] != '\0')
	{
		ft_putchar(s[x]);
		x++;
	}
	return (x);
}
